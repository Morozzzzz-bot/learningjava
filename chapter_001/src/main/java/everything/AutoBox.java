package everything;

public class AutoBox {
    public static void main(String[] args) {
        boolean b = false;
        Boolean bB = b;
        System.out.println(b);
        System.out.println(bB);
        char x = 'x';
        Character ch = x;
        System.out.println(x);
        System.out.println(ch);
        double a = 3.14;
        Double aA = a;
        System.out.println(a);
        System.out.println(aA);
    }

}
