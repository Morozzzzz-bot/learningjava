package everything;

public class AutoBox {
    public static void main(String[] args) {
        boolean b = false;
        Boolean B = b;
        System.out.println(b);
        System.out.println(B);
        char x = 'x';
        Character ch = x;
        System.out.println(x);
        System.out.println(ch);
        double a = 3.14;
        Double A = a;
        System.out.println(a);
        System.out.println(A);
    }

}
